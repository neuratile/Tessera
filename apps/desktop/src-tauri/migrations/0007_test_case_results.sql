-- Migration 0007 — per-case execution-outcome sidecar.
--
-- Backs the fixed 9-column Test Case table (plan/TEST_CASE_TABLE.md §4).
-- Columns 8–9 (Actual output / Result and remarks) are mutable,
-- user- and runtime-owned data that the LLM cannot know at generation
-- time. The artifact's `structured_data` JSON is regenerated on every
-- re-run, which would wipe those edits, so the outcomes live here —
-- keyed by (artifact_id, case_id) — and the table view LEFT JOINs the
-- LLM cases against this sidecar. Regeneration keeps the same `case.id`
-- values, so a row re-attaches to its case automatically.
--
-- Follows the 0004_test_runs.sql conventions (rules.md §2.3):
--   * TEXT UUID primary key generated by the application layer
--   * created_at / updated_at TEXT RFC-3339
--   * every foreign key is indexed
--   * snake_case names; parameterized SQL only at the call site
--
-- `result` mirrors the Rust `TestCaseResultStatus` serde enum and the
-- `source` column mirrors `TestCaseResultSource`
-- (repositories/test_case_result_repo.rs), and the Zod literals in
-- packages/shared/src/schemas/test-case-result.schema.ts exactly.

PRAGMA foreign_keys = ON;

-- ---------------------------------------------------------------------------
-- test_case_results — one current outcome per (artifact, case).
-- ---------------------------------------------------------------------------
CREATE TABLE test_case_results (
    id            TEXT PRIMARY KEY NOT NULL,
    artifact_id   TEXT NOT NULL,
    case_id       TEXT NOT NULL,              -- the TC-… id from structured_data
    actual_output TEXT,
    -- pass | fail | blocked | not_run
    result        TEXT NOT NULL DEFAULT 'not_run'
                  CHECK (result IN ('pass', 'fail', 'blocked', 'not_run')),
    remarks       TEXT,
    -- manual | sandbox
    source        TEXT NOT NULL DEFAULT 'manual'
                  CHECK (source IN ('manual', 'sandbox')),
    run_id        TEXT,                        -- nullable FK to test_runs for sandbox rows
    created_at    TEXT NOT NULL,
    updated_at    TEXT NOT NULL,
    FOREIGN KEY (artifact_id) REFERENCES artifacts(id) ON DELETE CASCADE,
    FOREIGN KEY (run_id) REFERENCES test_runs(id) ON DELETE SET NULL,
    UNIQUE (artifact_id, case_id)             -- one current outcome per case
);

CREATE INDEX idx_tc_results_artifact ON test_case_results(artifact_id);
CREATE INDEX idx_tc_results_run ON test_case_results(run_id);
